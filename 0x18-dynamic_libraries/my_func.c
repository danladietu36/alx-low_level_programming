#include "main.h"

/*
 * add - add two integers
 * @a: first integers
 * @b: second integers
 * return: addition of both integers
 *
 */
int add(int a, int b)
{
	int sum;
	sum = a + b;
	return (sum);
}

/*
 * sub - subtraction function
 * @a: first integers
 * @b: second integers
 * return: The result of both integers
 *
 */
int sub(int a, int b)
{
	int res;
	res = a - b;

	return (res);
}
